     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //create an DialogFlowUser object and assign values
        $user = new DialogFlowUser([
            'address' => $request->input('sessionInfo.parameters.collect-address-vic-only'),
            'propertyType' => $request->input('sessionInfo.parameters.collect-property-type'),
            'propertySituation' => $request->input('sessionInfo.parameters.collect-owner-property'),
            'solarSituation' => $request->input('sessionInfo.parameters.collect-solar-situation')
        ]);

        //save the user variable into the database
        $user->save();
    }

